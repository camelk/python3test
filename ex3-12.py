import turtle as t


t.begin_fill()
t.color("red")

t.forward(100)
t.right(60)

t.forward(100)
t.right(60)

t.forward(100)
t.right(60)

t.forward(100)
t.right(60)

t.forward(100)
t.right(60)

t.forward(100)
t.right(60)

t.end_fill()

t.penup()
t.goto(50,-80)
t.color("black")
t.pendown()
t.write("Stop", font = ("Arial", 18, "bold"))
t.hideturtle()

t.done()

